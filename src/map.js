function mapIsWall(x, y) {
  const mx = Math.floor(x), my = Math.floor(y);
  if (mx < 0 || mx >= MAP_W || my < 0 || my >= MAP_H) return true;
  return MAP[my][mx] === 1;
}

function mapIsOpen(x, y) {
  return !mapIsWall(x, y);
}
